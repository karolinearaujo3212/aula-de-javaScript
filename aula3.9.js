const calculadaora = { 
    somar: function (a, b) {
        return a + b; 
    }, 
    subtrair: function ( j, k) {
        return j - k 
    },
    multiplicar: function (l, h) {
        return l * h 
    }, 
    dividir: function (x, y) { 
        return x / y
    }
   
}; 
console.log (calculadaora.somar (5, 3));
console.log (calculadaora.subtrair (10, 4));
console.log (calculadaora.multiplicar (18, 14));
console.log (calculadaora.dividir (20, 10));